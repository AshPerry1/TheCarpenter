// Check if a week is unlocked
function isWeekUnlocked(weekNum) {
  const currentWeek = getWeekNumber();
  return weekNum <= currentWeek;
}

// Populate week selector
function populateWeekSelector() {
  const selector = document.getElementById('weekSelector');
  const currentWeek = getWeekNumber();
  
  for (let i = 1; i <= 52; i++) {
    const option = document.createElement('option');
    option.value = i;
    option.textContent = `Week ${i} of 52`;
    if (weeklyResources[i]) {
      option.textContent += ` - ${weeklyResources[i].theme}`;
    }
    
    // Lock future weeks
    if (!isWeekUnlocked(i)) {
      option.textContent += ' (Unlocks at midnight)';
      option.disabled = true;
    }
    
    if (i === currentWeek) {
      option.selected = true;
    }
    selector.appendChild(option);
  }
  
  // Add change event listener
  selector.addEventListener('change', (e) => {
    const selectedWeek = parseInt(e.target.value);
    if (isWeekUnlocked(selectedWeek)) {
      loadWeek(selectedWeek);
      document.getElementById('weekNumberDisplay').textContent = selectedWeek;
    }
  });
}

// Populate week cards in the browse section
function populateWeekCards() {
  const currentWeek = getWeekNumber();
  const grid = document.getElementById('weekCardsGrid');
  
  // Show first 12 weeks
  for (let i = 1; i <= 12; i++) {
    const week = weeklyResources[i];
    const isLocked = !isWeekUnlocked(i);
    
    const card = document.createElement('div');
    card.className = 'card';
    card.style.cursor = isLocked ? 'not-allowed' : 'pointer';
    card.style.opacity = isLocked ? '0.5' : '1';
    
    if (!isLocked) {
      card.onclick = () => loadWeek(i);
    }
    
    card.innerHTML = `
      <h3 class="card-title">Week ${i}: ${week.theme} ${isLocked ? '(Locked)' : ''}</h3>
      <p class="card-content">${week.subtitle}</p>
      <div class="verse-chip"><span class="verse-ref">${week.verse}</span></div>
    `;
    
    grid.appendChild(card);
  }
}

